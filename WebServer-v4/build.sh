#!/bin/bash

# 设置遇到错误立即停止运行
set -e

echo "================================================="
echo "       🚀 开始构建 WebServer-v4 项目"
echo "================================================="

# 1. 尝试清理旧的编译残留
echo "[1/3] 🧹 正在清理历史构建文件..."
make clean

# 2. 调用 make 进行多核编译（-j4 表示用 4 个核心并发编译，速度极快）
echo "[2/3] 🔨 正在编译源代码..."
make -j4

# 3. 检查当前目录下是否成功生成了 server 可执行文件
if [ -f "server" ]; then
    echo "[3/3] 🎉 构建成功！可执行文件已生成。"
    echo "================================================="
    echo " 💡 启动指南:"
    echo " -----------------------------------------------"
    echo " 默认端口启动: ./server"
    echo " 自定义启动:   ./server -p 9006 -t 8"
    echo " -----------------------------------------------"
    
    # 可选：询问是否立即启动
    read -p "是否立即启动服务器? [Y/n] " action
    if [[ "$action" == "Y" || "$action" == "y" || "$action" == "" ]]; then
        echo "🚀 正在启动服务器..."
        ./server -p 9006 -t 8
    else
        echo "👋 已退出，您可以稍后手动启动。"
    fi
else
    echo "❌ 致命错误：构建失败，未找到 server 可执行文件！"
    exit 1
fi